#include"libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ptr;
	int		i;

	ptr = b;
	i = 0;
	while (len > 0)
	{
		ptr[i] = c;
		i++;
		len--;
	}
	return (b);
}	
